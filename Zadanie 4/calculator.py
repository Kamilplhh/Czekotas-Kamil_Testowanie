def dod (x, y):
    return x + y

def odej (x, y):
    return x - y

def mnoz (x, y):
    return x * y

def dziel (x, y):
    return x / y

def potega(x,y):
    return x ** y

def pierw(x):
    return x ** 0.5

def procenty(x,y):
    return (100*x) / y 

def fah(x):
    return (x * 1.8) + 32

def kel(x):
    return x + 273.15
    
